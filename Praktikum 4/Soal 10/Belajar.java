public class Belajar{
	public static void main(String args []){
		int num=11; //variabel num bernilai 11 dan bertipe data int
		if(num>5) //jika nilai num lebih dari 5, akan mencetak "Number is greater than 5"
			System.out.println("Number is greater than 5"); //mencetak"Number is greater than 5"
		else //jika nilai num tidak lebih dari 5, akan mencetak "Number is less than 5"
			System.out.println("Number is less than 5");//"mencetak "Number is less than 5"
			//output adalah "Number is greater than 5" karena nilai dari variabel num adalah 11 dan lebih dari 5
	}
}