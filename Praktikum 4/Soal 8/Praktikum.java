public class Praktikum{
	public static void main(String args []){
		int count=1; //variabel count bernilai 1 dan bertipe data int
		while (count<5){ //selama count kurang dari 5 akan mencetak "count is equal to" ditambah perulangan nilai dari count yang bernilai awal 1
			System.out.println("Count is equal to"+ count);//mencetak "Count is equal to" 1,2,3,4 
			count++; //proses looping dari variabel count yang bernilai awal 1 dan jumlah kurang dari 5
			//sehingga hanya mencetak sampai 4
		}
	}
}