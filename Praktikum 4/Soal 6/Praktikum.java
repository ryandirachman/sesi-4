public class Praktikum{
	public static void main(String args []){
		boolean coba1;
		
		coba1=(10 == 10); //pernyataan bahwa nilai dari 10 sama dengan 10 dan pernyataan tersebut benar/true
			System.out.println(coba1);//mencetak nilai coba1
		coba1=(10 != 10);//pernyataan bahwa nilai dari 10 tidak sama dengan 10 dan pernyataan tersebut salah/false
			System.out.println(coba1);//mencetak nilai coba1
	}
}